function onFormSub(frmName){
	
	if(!onSubmitValidation(frmName.annualFeePrimary, 3, 2)){ return false; }
	if(!onSubmitValidation(frmName.annualFeeSecondary, 3, 2)){ return false; }
	if(!onSubmitValidation(frmName.issuerFee, 3, 2)){ return false; }
	if(!onSubmitValidation(frmName.replacementFee, 3, 2)){ return false; }
	if(!onSubmitValidation(frmName.cancelationFee, 3, 2)){ return false; }
	if(!onSubmitValidation(frmName.currConversionFee, 3, 2)){ return false; }
	if(!onSubmitValidation(frmName.adminstrationFee, 3, 2)){ return false; }
	
	if(!validInteger(frmName.joinFeePrimary)){ return false; }
	if(!validInteger(frmName.joinFeeSecondary)){ return false; }
	if(!validInteger(frmName.creditAdjFeePrimary)){ return false; }
	if(!validInteger(frmName.creditAdjFeeSecondary)){ return false; }
	if(!validInteger(frmName.urgentFeePrimary)){ return false; }
	if(!validInteger(frmName.urgentFeeSecondary)){ return false; }
	if(!validInteger(frmName.repinFeePrimary)){ return false; }
	if(!validInteger(frmName.repinFeeSecondary)){ return false; }
	
	return true;
	
}