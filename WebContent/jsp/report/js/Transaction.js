function disableOrEnable(){
	//alert('Test');
	var x = document.getElementById("TRANX_STATUS");
	var rc = document.getElementById("RESPONSE_CODE");
	
	//alert(x.value);
	//alert(rc);
	
	if(x.value != '' && x.value == 'A'){
		document.frmMain.RESPONSE_CODE[0].selected = "1";
		rc.disabled=true;
	}else{
		rc.disabled=false;
	}
	
}