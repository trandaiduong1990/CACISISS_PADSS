package org.transinfo.cacis.billing.utils;

import java.util.Comparator;

import org.transinfo.cacis.dto.transaction.TransactionCreditGLDto;

public class BillingComparator implements Comparator<TransactionCreditGLDto> {

	@Override
	public int compare(TransactionCreditGLDto obj1, TransactionCreditGLDto obj2) {
		return obj1.getDateTime().compareTo(obj2.getDateTime());
	}

}
