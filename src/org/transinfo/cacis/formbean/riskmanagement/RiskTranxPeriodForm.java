package org.transinfo.cacis.formbean.riskmanagement;

import java.util.Date;

import org.apache.struts.validator.ValidatorForm;

public class RiskTranxPeriodForm extends ValidatorForm{
    
        private String period = "";
        private String issuerId = "";
        private String tranxNo = "";       
        private Date updatedDate = new Date();
        private String userId = "";
       
                      
        public RiskTranxPeriodForm(){}
                
	public String getPeriod() {
		return period;
	}        
        public void setPeriod(String period) {
	       this.period = period;
	}
        
        public String getIssuerId() {
            return issuerId;
        }       
        public void setIssuerId(String issuerId) {
            this.issuerId = issuerId;
        }
	
        public String getTranxNo() {
		return tranxNo;
	}        
        public void setTranxNo(String tranxNo) {
               this.tranxNo = tranxNo;
	}
                                       
        public Date getUpdatedDate() {
		return updatedDate;
	}                
        public void setUpdatedDate(Date updatedDate) {
	      this.updatedDate = updatedDate;
	}
        
        public String getUserId() {
		return userId;
	}
        
        public void setUserId(String userId) {
	      this.userId = userId;
	}

           
}




