package org.transinfo.cacis.formbean.common;

import java.io.Serializable;

@SuppressWarnings("serial")
public class SalaryForm implements Serializable {

	private String cardProduct;
	private String creditLimit;
	private String cashAdvancedLimit;
	private String minSalary;
	private String maxSalary;

	public SalaryForm(){}

	public String getCardProduct() {
		return cardProduct;
	}

	public void setCardProduct(String cardProduct) {
		this.cardProduct = cardProduct;
	}

	public String getCreditLimit() {
		return creditLimit;
	}

	public void setCreditLimit(String creditLimit) {
		this.creditLimit = creditLimit;
	}

	public String getCashAdvancedLimit() {
		return cashAdvancedLimit;
	}

	public void setCashAdvancedLimit(String cashAdvancedLimit) {
		this.cashAdvancedLimit = cashAdvancedLimit;
	}

	public String getMinSalary() {
		return minSalary;
	}

	public void setMinSalary(String minSalary) {
		this.minSalary = minSalary;
	}

	public String getMaxSalary() {
		return maxSalary;
	}

	public void setMaxSalary(String maxSalary) {
		this.maxSalary = maxSalary;
	}

}
