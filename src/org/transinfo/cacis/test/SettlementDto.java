package org.transinfo.cacis.test;

import java.io.Serializable;

public class SettlementDto implements Serializable{
	
	public SettlementDto(){}
	
	private String issuerId;
	private long settlementId;
	private long cardNumber;
	private String currencyCode;
	private double amountCurr;
	private String refNumber;
	
	
	public long getCardNumber() {
		return cardNumber;
	}
	public void setCardNumber(long cardNumber) {
		this.cardNumber = cardNumber;
	}
	public String getIssuerId() {
		return issuerId;
	}
	public void setIssuerId(String issuerId) {
		this.issuerId = issuerId;
	}
	public long getSettlementId() {
		return settlementId;
	}
	public void setSettlementId(long settlementId) {
		this.settlementId = settlementId;
	}
	public String getCurrencyCode() {
		return currencyCode;
	}
	public void setCurrencyCode(String currencyCode) {
		this.currencyCode = currencyCode;
	}
	public double getAmountCurr() {
		return amountCurr;
	}
	public void setAmountCurr(double amountCurr) {
		this.amountCurr = amountCurr;
	}
	public String getRefNumber() {
		return refNumber;
	}
	public void setRefNumber(String refNumber) {
		this.refNumber = refNumber;
	}

}
