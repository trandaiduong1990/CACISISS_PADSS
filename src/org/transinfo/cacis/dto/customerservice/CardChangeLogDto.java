package org.transinfo.cacis.dto.customerservice;

import java.io.Serializable;
import java.util.Date;

@SuppressWarnings("serial")
public class CardChangeLogDto implements Serializable {

	private String cardChangeLogSerialNo;
	private String oldCarddNo;
	private String oldCardProduct;
	private String newCardNo;
	private String newCardProduct;
	private Character flag;
	private Date lastUpdatedDate;
	private String lastUpdateBy;

	public String getCardChangeLogSerialNo() {
		return cardChangeLogSerialNo;
	}

	public void setCardChangeLogSerialNo(String cardChangeLogSerialNo) {
		this.cardChangeLogSerialNo = cardChangeLogSerialNo;
	}

	public String getOldCardProduct() {
		return oldCardProduct;
	}

	public void setOldCardProduct(String oldCardProduct) {
		this.oldCardProduct = oldCardProduct;
	}

	public String getNewCardNo() {
		return newCardNo;
	}

	public void setNewCardNo(String newCardNo) {
		this.newCardNo = newCardNo;
	}

	public String getNewCardProduct() {
		return newCardProduct;
	}

	public void setNewCardProduct(String newCardProduct) {
		this.newCardProduct = newCardProduct;
	}

	public Character getFlag() {
		return flag;
	}

	public void setFlag(Character flag) {
		this.flag = flag;
	}

	public Date getLastUpdatedDate() {
		return lastUpdatedDate;
	}

	public void setLastUpdatedDate(Date lastUpdatedDate) {
		this.lastUpdatedDate = lastUpdatedDate;
	}

	public String getLastUpdateBy() {
		return lastUpdateBy;
	}

	public void setLastUpdateBy(String lastUpdateBy) {
		this.lastUpdateBy = lastUpdateBy;
	}

	public String getOldCarddNo() {
		return oldCarddNo;
	}

	public void setOldCarddNo(String oldCarddNo) {
		this.oldCarddNo = oldCarddNo;
	}
}
