package org.transinfo.cacis.dto.customerservice;

import java.io.Serializable;
import java.util.Collection;

@SuppressWarnings( { "serial", "unchecked" })
public class AddProductSearchDto implements Serializable {

	private String cardNo;
	private String customerName;
	private String mobileNo;
	private String collateralAcctId;

	private Collection searchList;
	private int totalCount;

	public String getCardNo() {
		return cardNo;
	}

	public void setCardNo(String cardNo) {
		this.cardNo = cardNo;
	}

	public String getCustomerName() {
		return customerName;
	}

	public void setCustomerName(String customerName) {
		this.customerName = customerName;
	}

	public String getMobileNo() {
		return mobileNo;
	}

	public void setMobileNo(String mobileNo) {
		this.mobileNo = mobileNo;
	}

	public String getCollateralAcctId() {
		return collateralAcctId;
	}

	public void setCollateralAcctId(String collateralAcctId) {
		this.collateralAcctId = collateralAcctId;
	}

	public Collection getSearchList() {
		return searchList;
	}

	public void setSearchList(Collection searchList) {
		this.searchList = searchList;
	}

	public int getTotalCount() {
		return totalCount;
	}

	public void setTotalCount(int totalCount) {
		this.totalCount = totalCount;
	}

}
