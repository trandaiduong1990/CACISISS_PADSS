package org.transinfo.cacis.dto.common;

import java.io.Serializable;

@SuppressWarnings("serial")
public class CardHolderTypeDto implements Serializable{

	private int cardHolderTypeId;
	private String description;

	public CardHolderTypeDto(){}


	public String getDescription() {
		return description;
	}
	public void setDescription(String description) {
		this.description = description;
	}


	public int getCardHolderTypeId() {
		return cardHolderTypeId;
	}


	public void setCardHolderTypeId(int cardHolderTypeId) {
		this.cardHolderTypeId = cardHolderTypeId;
	}





}
