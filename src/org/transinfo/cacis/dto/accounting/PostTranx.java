package org.transinfo.cacis.dto.accounting;

import java.io.Serializable;
import java.util.Date;

@SuppressWarnings("serial")
public class PostTranx implements Serializable {
	
	private String postId;
	private Long cardNo;
	private String accountId;
	private Long tranxLogId;
	private String billed;
	private Date postDate;
	
	public String getPostId() {
		return postId;
	}
	public void setPostId(String postId) {
		this.postId = postId;
	}
	public Long getCardNo() {
		return cardNo;
	}
	public void setCardNo(Long cardNo) {
		this.cardNo = cardNo;
	}
	public String getAccountId() {
		return accountId;
	}
	public void setAccountId(String accountId) {
		this.accountId = accountId;
	}
	public Long getTranxLogId() {
		return tranxLogId;
	}
	public void setTranxLogId(Long tranxLogId) {
		this.tranxLogId = tranxLogId;
	}
	public String getBilled() {
		return billed;
	}
	public void setBilled(String billed) {
		this.billed = billed;
	}
	public Date getPostDate() {
		return postDate;
	}
	public void setPostDate(Date postDate) {
		this.postDate = postDate;
	}

}
