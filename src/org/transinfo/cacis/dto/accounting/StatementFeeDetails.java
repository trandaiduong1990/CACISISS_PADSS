package org.transinfo.cacis.dto.accounting;

import java.io.Serializable;

@SuppressWarnings("serial")
public class StatementFeeDetails implements Serializable {
	
	private String fdetailId;
	private String feeId;
	private String statId;
	private String statGen;
	
	public String getFdetailId() {
		return fdetailId;
	}
	public void setFdetailId(String fdetailId) {
		this.fdetailId = fdetailId;
	}
	public String getFeeId() {
		return feeId;
	}
	public void setFeeId(String feeId) {
		this.feeId = feeId;
	}
	public String getStatId() {
		return statId;
	}
	public void setStatId(String statId) {
		this.statId = statId;
	}
	public String getStatGen() {
		return statGen;
	}
	public void setStatGen(String statGen) {
		this.statGen = statGen;
	}

}
