package org.transinfo.cacis.dto.disputemanagement;

import java.io.Serializable;
import java.util.Date;

@SuppressWarnings("serial")
public class DisputeCaseEventDto implements Serializable {

	private String sno;
	private DisputeEventsDto disputEevent;
	private DisputeMasterDto disputeMaster;
	private Date dateTime;
	private Base2DocIndicatorDto documentIncluded;
	//private String documentIncluded;
	private String remarks;
	private String arn;

	public String getSno() {
		return sno;
	}

	public void setSno(String sno) {
		this.sno = sno;
	}

	public DisputeEventsDto getDisputEevent() {
		return disputEevent;
	}

	public void setDisputEevent(DisputeEventsDto disputEevent) {
		this.disputEevent = disputEevent;
	}

	public DisputeMasterDto getDisputeMaster() {
		return disputeMaster;
	}

	public void setDisputeMaster(DisputeMasterDto disputeMaster) {
		this.disputeMaster = disputeMaster;
	}

	public Date getDateTime() {
		return dateTime;
	}

	public void setDateTime(Date dateTime) {
		this.dateTime = dateTime;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public Base2DocIndicatorDto getDocumentIncluded() {
		return documentIncluded;
	}

	public void setDocumentIncluded(Base2DocIndicatorDto documentIncluded) {
		this.documentIncluded = documentIncluded;
	}

	public String getArn() {
		return arn;
	}

	public void setArn(String arn) {
		this.arn = arn;
	}
}
