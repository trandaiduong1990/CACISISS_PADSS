package org.transinfo.cacis.dto.disputemanagement;

import java.io.Serializable;

@SuppressWarnings("serial")
public class DisputeGroupDetailsDto implements Serializable {

	private String reasonCode;
	private String chargeBackReason;
	private String timeLimit;
	private Integer minCBAmt;
	private Integer minCBTEAmt;
	private DisputeGroupsDto disputeGroup;

	public String getReasonCode() {
		return reasonCode;
	}

	public void setReasonCode(String reasonCode) {
		this.reasonCode = reasonCode;
	}

	public String getChargeBackReason() {
		return chargeBackReason;
	}

	public void setChargeBackReason(String chargeBackReason) {
		this.chargeBackReason = chargeBackReason;
	}

	public String getTimeLimit() {
		return timeLimit;
	}

	public void setTimeLimit(String timeLimit) {
		this.timeLimit = timeLimit;
	}

	public Integer getMinCBAmt() {
		return minCBAmt;
	}

	public void setMinCBAmt(Integer minCBAmt) {
		this.minCBAmt = minCBAmt;
	}

	public Integer getMinCBTEAmt() {
		return minCBTEAmt;
	}

	public void setMinCBTEAmt(Integer minCBTEAmt) {
		this.minCBTEAmt = minCBTEAmt;
	}

	public DisputeGroupsDto getDisputeGroup() {
		return disputeGroup;
	}

	public void setDisputeGroup(DisputeGroupsDto disputeGroup) {
		this.disputeGroup = disputeGroup;
	}
}
