package org.transinfo.cacis.dto.disputemanagement;

import java.io.Serializable;
import java.util.Date;

public class DisputeFormRemarksDto implements Serializable {
	
	
	private static final long serialVersionUID = 1L;
	private String remarksId;
	private String claimNumber;
	private String communicationType;
	private String remarks;
	private Date claimDate;
	private char action;
	
	public DisputeFormRemarksDto(){}

	public Date getClaimDate() {
		return claimDate;
	}

	public void setClaimDate(Date claimDate) {
		this.claimDate = claimDate;
	}

	public String getClaimNumber() {
		return claimNumber;
	}

	public void setClaimNumber(String claimNumber) {
		this.claimNumber = claimNumber;
	}

	public String getRemarks() {
		return remarks;
	}

	public void setRemarks(String remarks) {
		this.remarks = remarks;
	}

	public char getAction() {
		return action;
	}

	public void setAction(char action) {
		this.action = action;
	}

	public String getCommunicationType() {
		return communicationType;
	}

	public void setCommunicationType(String communicationType) {
		this.communicationType = communicationType;
	}

	public String getRemarksId() {
		return remarksId;
	}

	public void setRemarksId(String remarksId) {
		this.remarksId = remarksId;
	}
	
}
