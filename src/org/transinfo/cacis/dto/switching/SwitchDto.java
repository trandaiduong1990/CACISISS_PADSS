package org.transinfo.cacis.dto.switching;

import java.io.Serializable;
import java.util.Date;

@SuppressWarnings("serial")
public class SwitchDto implements Serializable{
	
	private String swName;	
	private String swType;
	private String swIpAddress;
	private String swPort; 
	private int swTimeOut1;
	private int swTimeOut2;
	private Date updatedDate = new Date();
	private String userId;

	public SwitchDto() {}        

	public String getSwName() {
		return swName;
	}

	public void setSwName(String swName) {
		this.swName = swName;
	}

	public String getSwType() {
		return swType;
	}

	public void setSwType(String swType) {
		this.swType = swType;
	}

	public String getSwIpAddress() {
		return swIpAddress;
	}

	public void setSwIpAddress(String swIpAddress) {
		this.swIpAddress = swIpAddress;
	}

	public String getSwPort() {
		return swPort;
	}

	public void setSwPort(String swPort) {
		this.swPort = swPort;
	}

	public int getSwTimeOut1() {
		return swTimeOut1;
	}

	public void setSwTimeOut1(int swTimeOut1) {
		this.swTimeOut1 = swTimeOut1;
	}

	public int getSwTimeOut2() {
		return swTimeOut2;
	}

	public void setSwTimeOut2(int swTimeOut2) {
		this.swTimeOut2 = swTimeOut2;
	}

	public Date getUpdatedDate() {
		return updatedDate;
	}

	public void setUpdatedDate(Date updatedDate) {
		this.updatedDate = updatedDate;
	}

	public String getUserId() {
		return userId;
	}

	public void setUserId(String userId) {
		this.userId = userId;
	}



}

