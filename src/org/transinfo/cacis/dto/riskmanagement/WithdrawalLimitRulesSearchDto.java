package org.transinfo.cacis.dto.riskmanagement;

import java.io.Serializable;

public class WithdrawalLimitRulesSearchDto implements Serializable {

	private long ruleId;
    private String tranxType;

	public WithdrawalLimitRulesSearchDto(){}

	public long getRuleId() {
		return new Long(ruleId).longValue();
	}

	public void setRuleId(long ruleId) {
		this.ruleId = ruleId;
	}

	public String getTranxType() {
		return tranxType;
	}

	public void setTranxType(String tranxType) {
		this.tranxType = tranxType;
	}

}
