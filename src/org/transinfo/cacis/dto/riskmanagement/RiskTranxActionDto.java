package org.transinfo.cacis.dto.riskmanagement;

import java.io.Serializable;
import java.util.Date;

public class RiskTranxActionDto implements Serializable{
        
        private String riskId;
        private String action;
        private String email;
        private String smsAlert;
        private String status;
        private Date updatedDate = new Date();
        private String userId;

       

        public RiskTranxActionDto(){}

     
               
        public String getRiskId() {
            return riskId;
        }                
        public void setRiskId(String riskId) {
            this.riskId = riskId;
        }
       
         public String getAction() {
            return action;
        }        
       
        public void setAction(String action) {
            this.action = action;
        }
        
        
        public String getEmail() {
            return email;
        }
        
        public void setEmail(String email) {
            this.email = email;
        }
        
       
        public String getSmsAlert() {
           
            return smsAlert;                    
        }       
        
        public void setSmsAlert(String smsAlert) {
            
            this.smsAlert = smsAlert;                       
        }
        
        public String getStatus() {
            return status;
        }        
       
        public void setStatus(String status) {
            this.status = status;
        }     
        
        
        public Date getUpdatedDate() {
		return updatedDate;
	}
        public void setUpdatedDate(Date updatedDate) {
	      this.updatedDate = updatedDate;
	}

        public String getUserId() {
		return userId;
	}
        public void setUserId(String userId) {
	      this.userId = userId;
	}

       
          
               
}
